#include "3-calc.h"

/**
 * op_add - Function that find the summation two integers
 * @a: param1
 * @b: param2
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that performs subtraction on two integers
 * @a: param1
 * @b: param2
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function that add two integers
 * @a: param1
 * @b: param2
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function that add two integers
 * @a: param1
 * @b: param2
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function that add two integers
 * @a: param1
 * @b: param2
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}





































